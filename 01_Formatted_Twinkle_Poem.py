# Рядки (strings)
# Рядки пишуться в одинарних ' або подвійних " лапках.
# Можна використовувати потрійні лапки ''' або """ для багатострокового тексту.

# Символи відступу
# \n - перенос рядка
# Табуляція — це \t
# Один \t = 4 пробіли (зазвичай)
# Пробіли також можна писати вручну: '    ' (4 пробіли)

# Функція print()
# Виводить текст на екран.

# Варіант 1
print("Twinkle, twinkle, little star, \n\tHow I wonder what you are! \n\t\tUp above the world so high, \n\t\tLike a diamond in the sky. \nTwinkle, twinkle, little star, \n\tHow I wonder what you are");

# Варіант 2
print("Twinkle, twinkle, little star,")
print("\tHow I wonder what you are!")
print("\t\tUp above the world so high,")
print("\t\tLike a diamond in the sky.")
print("Twinkle, twinkle, little star,")
print("\tHow I wonder what you are")

